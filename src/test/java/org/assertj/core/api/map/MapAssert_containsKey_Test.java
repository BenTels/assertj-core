package org.assertj.core.api.map;

import static org.assertj.core.api.BDDAssertions.then;
import static org.assertj.core.util.AssertionsUtil.expectAssertionError;
import static org.assertj.core.error.ShouldContainKeys.shouldContainKeys;
import static org.assertj.core.api.Assertions.assertThat;
import static org.assertj.core.util.Arrays.array;
import static org.mockito.Mockito.verify;

import java.util.HashSet;
import java.util.Map;
import java.util.Set;

import org.assertj.core.api.Condition;
import org.assertj.core.api.MapAssert;
import org.assertj.core.api.MapAssertBaseTest;
import org.junit.jupiter.api.Test;


/**
 * Tests for <code>{@link MapAssert#containsKey(Object)}</code>.
 *
 * @author Nicolas Fran√ßois
 */
class MapAssert_containsKey_Test extends MapAssertBaseTest {

  @Override
  protected MapAssert<Object, Object> invoke_api_method() {
    return assertions.containsKey("key1");
  }

  @Override
  protected void verify_internal_effects() {
    verify(maps).assertContainsKeys(getInfo(assertions), getActual(assertions), array("key1"));
  }
  
  @Test
  protected void should_fail_when_asserting_for_null_on_map_that_does_not_contian_null() {
    Set<String> helper = new HashSet<>();
    helper.add(null);
    
    // GIVEN
    Map<String, String> actual = Map.of("whatever", "don't care");
    // WHEN
    AssertionError assertionError = expectAssertionError(() -> assertThat(actual).containsKey(null));
    // THEN
    /* This is not ideal. It feels to me like this should be a call to 
     * ShouldContainKey.shouldContainKey(). However, internally the assertion
     * error is generated by ShouldContainKeys -- and that has a different 
     * error message.
     */
    then(assertionError).hasMessage(shouldContainKeys(actual, helper).create());
  }
}
